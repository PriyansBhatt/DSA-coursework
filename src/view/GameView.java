    private void tblGamesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGamesMouseClicked
    int row = tblGames.getSelectedRow();
    if (row == -1) return;

    txtGameID.setText(model.getValueAt(row, 0).toString());
    txtGameName.setText(model.getValueAt(row, 1).toString());
    txtMinBet.setText(model.getValueAt(row, 2).toString());
    txtMaxBet.setText(model.getValueAt(row, 3).toString());
    cmbStatus.setSelectedItem(model.getValueAt(row, 4).toString());        
    // TODO add your handling code here:
    }//GEN-LAST:event_tblGamesMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    int row = tblGames.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a row to update.");
        return;
    }

    try {
        String id = txtGameID.getText().trim();
        String name = txtGameName.getText().trim();
        double minBet = Double.parseDouble(txtMinBet.getText().trim());
        double maxBet = Double.parseDouble(txtMaxBet.getText().trim());
        String status = cmbStatus.getSelectedItem().toString();

        if (id.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID and Name are required.");
            return;
        }
        if (minBet < 0 || maxBet < 0 || maxBet < minBet) {
            JOptionPane.showMessageDialog(this, "Invalid bet range.");
            return;
        }

        // Update table
        model.setValueAt(id, row, 0);
        model.setValueAt(name, row, 1);
        model.setValueAt(minBet, row, 2);
        model.setValueAt(maxBet, row, 3);
        model.setValueAt(status, row, 4);

        // Update list too (same index)
        Game g = gameList.get(row);
        g.setId(id);
        g.setName(name);
        g.setMinBet(minBet);
        g.setMaxBet(maxBet);
        g.setStatus(status);

        clearFields();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Min Bet and Max Bet must be numbers.");
    }
    // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int row = tblGames.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a row to delete.");
        return;
    }

    controller.deleteGame(row);
    gameList = controller.getAllGames();
    FileUtil.saveToFile(gameList, "games.dat");

    refreshTable();
    clearFields();
    // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

