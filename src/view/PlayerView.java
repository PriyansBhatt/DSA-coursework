    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!validateInputs()) {
            return;
        }

        String id = txtID.getText().trim();

        if (isDuplicateId(id)) {
            JOptionPane.showMessageDialog(this, "Player ID already exists!");
            return;
        }

        String name = txtName.getText().trim();
        int age = Integer.parseInt(txtAge.getText().trim());
        double chips = Double.parseDouble(txtChips.getText().trim());
        String status = cmbStatus.getSelectedItem().toString();
        String contact = txtContact.getText().trim();

        Player p = new Player(id, name, age, chips, status, contact);
        controller.addPlayer(p);
        playerList = controller.getAllPlayers();
        FileUtil.saveToFile(playerList, "players.dat");

        refreshTable();
        clearFields();

// TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

