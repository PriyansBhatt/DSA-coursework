    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String id = jTextField5.getText();
            String name = jTextField1.getText();
            int age = Integer.parseInt(jTextField2.getText());
            int chips = Integer.parseInt(jTextField3.getText());
            String status = jTextField4.getText();

            if (name.isEmpty() || status.isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields are required");
                return;
            }

            Player p = new Player(id, name, age, chips, status);
            boolean added = playerController.addPlayer(p);

            if (!added) {
                JOptionPane.showMessageDialog(
                        this,
                        "Player ID already exists",
                        "Duplicate ID",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }
            clearPlayerForm();
            loadPlayersTable();
            updateTotalPlayers();

            JOptionPane.showMessageDialog(this, "Player added successfully");
            if (playerController.addPlayer(p)) {
                JOptionPane.showMessageDialog(this, "Failed to add player");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Age and Chips must be numbers",
                    "Input Error",
                    JOptionPane.ERROR_MESSAGE
            );
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error adding player",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }       // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

