    private void loadPlayersTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); // Recently Added table
        DefaultTableModel pModel = (DefaultTableModel) tblPlayers.getModel();
        model.setRowCount(0); // clear table
        pModel.setRowCount(0);
        for (Player p : playerController.getAllPlayers()) {
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getAge(),
                p.getChips(),
                p.getStatus()
            });
        }
        for (Player p : playerController.getAllPlayers()) {
            pModel.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getAge(),
                p.getChips(),
                p.getStatus()
            });
        }

    }

    private void loadGamesTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        DefaultTableModel gmodel = (DefaultTableModel) tblGames.getModel();
        model.setRowCount(0);
        gmodel.setRowCount(0);
        for (Game g : gameController.getAllGames()) {
            model.addRow(new Object[]{
                g.getId(),
                g.getName(),
                g.getMaxBet(),
                g.getMinBet(),
                g.getPlayerName()
            });
            gmodel.addRow(new Object[]{
                g.getId(),
                g.getName(),
                g.getMaxBet(),
                g.getMinBet(),
                g.getPlayerName()
            });
        }
    }

