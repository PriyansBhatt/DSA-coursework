    private void refreshTable() {
        model.setRowCount(0);
        for (Player p : playerList) {
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getAge(),
                p.getChips(),
                p.getStatus(),
                p.getContact()
            });
        }
    }

    private void clearFields() {
        txtID.setText("");
        txtName.setText("");
        txtAge.setText("");
        txtChips.setText("");
        cmbStatus.setSelectedIndex(0);
        txtContact.setText("");
    }

    private boolean validateInputs() {

        if (txtID.getText().trim().isEmpty()
                || txtName.getText().trim().isEmpty()
                || txtAge.getText().trim().isEmpty()
                || txtChips.getText().trim().isEmpty()
                || txtContact.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "All fields are required!");
            return false;
        }

        try {
            int age = Integer.parseInt(txtAge.getText().trim());
            if (age <= 0) {
                JOptionPane.showMessageDialog(this, "Age must be positive!");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Age must be a valid integer!");
            return false;
        }

        try {
            double chips = Double.parseDouble(txtChips.getText().trim());
            if (chips < 0) {
                JOptionPane.showMessageDialog(this, "Chips cannot be negative!");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Chips must be a valid number!");
            return false;
        }

        return true;
    }

    private boolean isDuplicateId(String id) {
        for (Player p : playerList) {
            if (p.getId().equalsIgnoreCase(id)) {
                return true;
            }
        }
        return false;
    }

