    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblPlayers.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a player to update.");
            return;
        }

        if (!validateInputs()) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to update this player?",
                "Confirm Update",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String id = txtID.getText().trim();
        String name = txtName.getText().trim();
        int age = Integer.parseInt(txtAge.getText().trim());
        double chips = Double.parseDouble(txtChips.getText().trim());
        String status = cmbStatus.getSelectedItem().toString();
        String contact = txtContact.getText().trim();

        Player p = new Player(id, name, age, chips, status, contact);

        controller.updatePlayer(row, p);
        playerList = controller.getAllPlayers();
        FileUtil.saveToFile(playerList, "players.dat");

        refreshTable();
        clearFields();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

