    private void btnLinearSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLinearSearchActionPerformed
        String key = txtSearch.getText().trim();

        if (key.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter ID or Name to search.");
            return;
        }
        Player p = controller.linearSearch(key);
        model.setRowCount(0); // clear table

        if (p != null) {
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getAge(),
                p.getChips(),
                p.getStatus(),
                p.getContact()
            });
        } else {
            JOptionPane.showMessageDialog(this, "No player found.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnLinearSearchActionPerformed

