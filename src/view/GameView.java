    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    try {
        String id = txtGameID.getText().trim();
        String name = txtGameName.getText().trim();
        double minBet = Double.parseDouble(txtMinBet.getText().trim());
        double maxBet = Double.parseDouble(txtMaxBet.getText().trim());
        String status = cmbStatus.getSelectedItem().toString();

        if (id.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID and Name are required.");
            return;
        }
        if (minBet < 0 || maxBet < 0 || maxBet < minBet) {
            JOptionPane.showMessageDialog(this, "Invalid bet range.");
            return;
        }

        Game g = new Game(id, name, minBet, maxBet, status);
        controller.addGame(g);
        
        gameList = controller.getAllGames();
        FileUtil.saveToFile(gameList, "games.dat");

        model.addRow(new Object[]{
            g.getId(),
            g.getName(),
            g.getMinBet(),
            g.getMaxBet(),
            g.getStatus()
        });
        refreshTable();
        clearFields();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Min Bet and Max Bet must be numbers.");
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

